    public boolean check(){
        String newcard = jTextField4.getText();
        String oldcard = model.getValueAt(rowIndex, 3).toString();
        if(newcard.equals(oldcard)){
            return false;
        }else{
            if(!newcard.equals(oldcard)){
                boolean x= Student.isCardExist(newcard);
                if(x){
                    JOptionPane.showMessageDialog(this, "This Card Number already exists");
                }
                return x;
            }
        }
        return false;
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(isEmptyStudent()){
            if (!check()) {
                String admission = jTextField1.getText();
                String sname = jTextField2.getText();
                String pnumber = jTextField3.getText();
                String card = jTextField4.getText();
                String balance = jTextField5.getText();
                String status = jComboBox1.getSelectedItem().toString();
                Student.update(admission, sname, pnumber, card, balance, status);
                clear();
                jTable1.setModel(new DefaultTableModel(null, new Object[]{"Student Admission", "Student Name", "Parent Number", "Card Number", "Balance", "Status"}));
                Student.getStudentValue(jTable1, " ");
                clear();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(isEmptyStudent()){
            if(!Student.isCardExist(jTextField4.getText())){
                String admission = jTextField1.getText();
                String sname = jTextField2.getText();
                String pnumber = jTextField3.getText();
                String card = jTextField4.getText();
                String balance = jTextField5.getText();
                String status = jComboBox1.getSelectedItem().toString();
                Student.insert(admission, sname, pnumber, card, balance, status);
                clear(); 
                jTable1.setModel(new DefaultTableModel(null, new Object[]{"Student Admission","Student Name","Parent Number","Card Number","Balance","Status"}));
                Student.getStudentValue(jTable1," ");
                clear(); 
            }
            else{
                JOptionPane.showMessageDialog(this, "Student Card Number Already Exists");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

